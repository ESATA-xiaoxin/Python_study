print('------------------位运算---------------------')
print(4&8)  #按位与&，同为1时结果为1
print(4|8)  #按位或|，同为0时结果为0

print(4<<1)  #向左移动1位（移动一个位置），相当于乘以2
print(4<<2)  #向左移动2位（移动两个位置）,相当于乘以4

print(4>>1)  #向右移动1位，相当于除以2
print(4>>2)


#运算顺序：（）括号-->算术运算-->位运算-->比较运算-->布尔运算-->赋值运算

